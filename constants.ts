// 1. HELPER: PARSER FOR USER RAW TEXT
// This parser respects the rule: Question starts with "Number : " and ends before the next "Number : "
function parseRawQuestions(raw: string): Question[] {
  const questions: Question[] = [];
  // Regex to find start of lines like "1 :" or "1\t:"
  const lines = raw.split(/\n/);
  let currentId = '';
  let currentBuffer: string[] = [];

  const flush = () => {
    if (!currentId || currentBuffer.length === 0) return;
    
    // Join lines
    let fullText = currentBuffer.join('\n').trim();
    
    // Remove surrounding quotes if they wrap the *entire* content
    // Regex checks if it starts with " and ends with "
    if (fullText.startsWith('"') && fullText.endsWith('"')) {
       fullText = fullText.substring(1, fullText.length - 1).trim();
    }

    // Split Title vs Description
    // Strategy: The first line (or sentence ending in ?) is the main text.
    // The rest is description.
    const splitIndex = fullText.indexOf('\n');
    let text = '';
    let description = '';

    if (splitIndex !== -1) {
        text = fullText.substring(0, splitIndex).trim();
        description = fullText.substring(splitIndex + 1).trim();
    } else {
        text = fullText;
    }

    // Clean up "مثلاً :" from description start if present
    if (description.startsWith('مثلاً :') || description.startsWith('مثلا :')) {
        // keep it as is, it's good context
    }

    // Determine Category
    let category: any = 'General';
    const t = text + description;
    if (t.includes("خدا") || t.includes("نماز") || t.includes("دین") || t.includes("قرآن") || t.includes("معنوی")) category = 'Religious';
    else if (t.includes("پدر") || t.includes("مادر") || t.includes("خانواده") || t.includes("خواهر")) category = 'Family';
    else if (t.includes("دوست") || t.includes("رفیق") || t.includes("اجتماعی") || t.includes("جمع")) category = 'Social';
    else if (t.includes("جنسی") || t.includes("لخت") || t.includes("بغل") || t.includes("اندام") || t.includes("عشق")) category = 'Sexual';
    else if (t.includes("دروغ") || t.includes("صداقت") || t.includes("اعتماد")) category = 'Honesty';
    else if (t.includes("آینده") || t.includes("شغل") || t.includes("پول") || t.includes("ثروت")) category = 'Future';
    else if (t.includes("افسرد") || t.includes("غم") || t.includes("خودکشی") || t.includes("ترس")) category = 'Personal';
    else if (t.includes("علم") || t.includes("درس") || t.includes("کتاب") || t.includes("هوش")) category = 'Intellectual';

    // Determine Type
    // User requested mostly Text (Descriptive).
    // Only use Likert if explicitly asking for agreement intensity regarding a statement.
    let type = QuestionType.Text;
    if (text.includes("موافقم") || text.includes("مخالفم") || text.includes("نمره از 1 تا")) {
        type = QuestionType.Likert;
    }

    questions.push({
      id: `uq_${currentId}`,
      text: text,
      description: description || undefined,
      type,
      category
    });
  };

  const startRegex = /^(\d+)\s*:\s*(.*)/;

  for (const line of lines) {
    const match = line.match(startRegex);
    if (match) {
      flush(); // Save previous
      currentId = match[1];
      currentBuffer = [match[2]]; // Start new buffer with the rest of the line
    } else {
      if (currentId) {
        currentBuffer.push(line); // Append to current
      }
    }
  }
  flush(); // Save last
  return questions;
}

